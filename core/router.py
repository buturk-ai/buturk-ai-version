def analyze_and_route(prompt):
    if "لعبة" in prompt:
        return "🧠 يبدو أنك تريد إنشاء لعبة. أخبرني بالتفاصيل لأوجهك لوكيل الألعاب."
    elif "موقع" in prompt:
        return "🌐 تريد تطوير موقع؟ ممتاز! أخبرني بفكرتك لأوجهك لوكيل تطوير الويب."
    elif "تصميم" in prompt:
        return "🎨 أنت بحاجة لتصميم؟ سيتم التوجيه لوكيل التصميم."
    elif "خطة" in prompt or "فكرة" in prompt:
        return "🧩 نبدأ بتخطيط الفكرة! أخبرني بالمزيد لأحللها لك."
    elif "كود" in prompt or "برمجة" in prompt:
        return "💻 ستتم برمجة الفكرة! سأوجهها لوكيل كتابة الأكواد."
    elif "اختبار" in prompt:
        return "🧪 سيتم توجيه الفكرة لوكيل الاختبار الآن."
    else:
        return "🤖 لم أفهم المطلوب تمامًا، أعد صياغة السؤال أو أخبرني ما الذي تريده تحديدًا."
